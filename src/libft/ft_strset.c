#include "libft.h"

char	*strset(char c, int len)
{
	char	*str;
	int		i;

	if (!(str = ft_strnew(len)))
		return ((void *)0);
	i = 0;
	while (i < len + 1)
		str[i++] = c;
	str[i] = '\0';
	return (str);
}
